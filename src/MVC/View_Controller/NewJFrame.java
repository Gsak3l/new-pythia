    private void loginButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginButtonActionPerformed
        FileInputStream fi=null;
        ObjectInputStream oi=null;
        try {
            switch(loginAs){
                case "Admin":
                    fi = new FileInputStream(new File("Admin.txt"));
                    oi = new ObjectInputStream(fi);
                    break;
                case "Student":
                    fi = new FileInputStream(new File("Foithtes.txt"));
                    oi = new ObjectInputStream(fi);
                    break;
                case "Professor":
                    fi = new FileInputStream(new File("Professors.txt"));
                    oi = new ObjectInputStream(fi);
                    System.out.println("eftasa");
                    break;
            }
            Account a1;
            String usernameTextfield = usernameLogin.getText();
            char[] passwordTextfield = passwordLogin.getPassword();
            while (true){
                try{
                    a1=(Account)oi.readObject();
                    if(a1 instanceof Admin){
                        admin = (Admin)a1;
                    } else if(a1 instanceof Foititis) {
                        std = (Foititis)a1; 
                    } else if(a1 instanceof Kathigitis){
                        prof = (Kathigitis)a1;
                    }
                    char[] passwordArray = a1.getPassword().toCharArray();
                    if(Arrays.equals(passwordTextfield, passwordArray) && a1.getUsername().equals(usernameTextfield)){
                        getContentPane().removeAll();
                        getContentPane().repaint();
                        getContentPane().revalidate();
                        switch (a1.getIdiotita()) {
                            case "Administrator":
                                getContentPane().add(AdminPage);
                                if(admin.getDilwseis()){
                                    DhloseisEnable.setText("Periodos Diloseon");
                                    System.out.println(admin.getDilwseis());
                                }else{
                                    DhloseisEnable.setText("Enable Diloseis");
                                    System.out.println(admin.getDilwseis());
                                }
                                break;
                            case "Professor":
                                getContentPane().add(KathigitisPage);
                                break;
                            case "Student":
                                getContentPane().add(FoiththsPage);
                                ShowAmStd.setText(String.valueOf(std.getAM()));
                                ShowExaminoStd.setText(String.valueOf(std.getEksamino()));
                                ShowDieuthinsiStd.setText(std.getDieuthinsi()); 
                                ShowTmimaStd.setText(std.getTmima());
                                ShowStoixiaEggrafisStd.setText(String.valueOf(std.getStoixeiaEggrafis()));
                                break;
                            default:
                                break;
                        }
                        getContentPane().repaint();
                        getContentPane().revalidate();
                        break;
                    }
                }catch (EOFException ex1) {
                    break; //EOF reached.
                }catch (IOException ex2) {
                    resetAccountFile();
                    JOptionPane.showMessageDialog(null, "File was reset");
                    loginButtonActionPerformed(evt);
                    break;
                }catch (ClassNotFoundException ex) {
                    System.out.println("Ex1");
                    Logger.getLogger(NewJFrame.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        } catch (IOException ex) {
            resetAccountFile();
            JOptionPane.showMessageDialog(null, "Someone changed or removed the file manually, File is resetting! Contact an admin");
        }finally{
            try{
                oi.close();
                fi.close();
                System.out.println("ekleisa");
            } catch (IOException ex) {
                Logger.getLogger(NewJFrame.class.getName()).log(Level.SEVERE, null, ex);
            }
            clearLoginFields();
        }
    }//GEN-LAST:event_loginButtonActionPerformed

